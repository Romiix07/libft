#include <stdlib.h>
#include "libft.h"

char	*ft_strdup(char const *src)
{
	char	*str;
	int		i;

	if (src == NULL)
		return (NULL);
	if ((str = malloc(sizeof(char) * (ft_strlen(src) + 1))) == NULL)
		return (NULL);
	i = -1;
	while (src[++i])
		str[i] = src[i];
	str[i] = '\0';
	return (str);
}
